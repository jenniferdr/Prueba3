package com.example.jennifer.prueba3;

import java.util.Date;

public class GoodNew {

    private String goodNew;
    private String date;
    private String id;
    private Boolean favorite;

    public GoodNew() {
    }

    public String getGoodNew() {
        return goodNew;
    }

    public void setGoodNew(String goodNew) {
        this.goodNew = goodNew;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public Boolean getFavorite() {
        return favorite;
    }

    public void setFavorite(Boolean favorite) {
        this.favorite = favorite;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
